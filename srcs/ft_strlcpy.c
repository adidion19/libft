#include <stdlib.h>

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	int i;

	if (!dest || !src)
		return (0);
	while (src[i])
		i++;
	if (size == 0)
		return ((size_t)i);
	i = 0;
	while (src[i] && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	i = 0;
	while (src[i])
		i++;
	return ((size_t)i);
}
