#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && i < n - 1 && s1[i])
		i++;
	return ((int)s1[i] - (int)s2[i]);
}
